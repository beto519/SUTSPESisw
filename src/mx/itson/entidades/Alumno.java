package mx.itson.entidades;

/**
 *
 * @author vinko
 */
public class Alumno {
    private int idAlumno;
    private String Nombre;
    private String Nivel;
    private int Edad;
    private String Imagen;
    private String NomImagen;
    private String NombreTutor;
    private String Telefono;
    private double porcentaje;

    /**
     * @return the idAlumno
     */
    public int getIdAlumno() {
        return idAlumno;
    }

    /**
     * @param idAlumno the idAlumno to set
     */
    public void setIdAlumno(int idAlumno) {
        this.idAlumno = idAlumno;
    }

    /**
     * @return the Nombre
     */
    public String getNombre() {
        return Nombre;
    }

    /**
     * @param Nombre the Nombre to set
     */
    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    /**
     * @return the Nivel
     */
    public String getNivel() {
        return Nivel;
    }

    /**
     * @param Nivel the Nivel to set
     */
    public void setNivel(String Nivel) {
        this.Nivel = Nivel;
    }

    /**
     * @return the Edad
     */
    public int getEdad() {
        return Edad;
    }

    /**
     * @param Edad the Edad to set
     */
    public void setEdad(int Edad) {
        this.Edad = Edad;
    }

    /**
     * @return the Imagen
     */
    public String getImagen() {
        return Imagen;
    }

    /**
     * @param Imagen the Imagen to set
     */
    public void setImagen(String Imagen) {
        this.Imagen = Imagen;
    }

    /**
     * @return the NomImagen
     */
    public String getNomImagen() {
        return NomImagen;
    }

    /**
     * @param NomImagen the NomImagen to set
     */
    public void setNomImagen(String NomImagen) {
        this.NomImagen = NomImagen;
    }

    /**
     * @return the NombreTutor
     */
    public String getNombreTutor() {
        return NombreTutor;
    }

    /**
     * @param NombreTutor the NombreTutor to set
     */
    public void setNombreTutor(String NombreTutor) {
        this.NombreTutor = NombreTutor;
    }

    /**
     * @return the Telefono
     */
    public String getTelefono() {
        return Telefono;
    }

    /**
     * @param Telefono the Telefono to set
     */
    public void setTelefono(String Telefono) {
        this.Telefono = Telefono;
    }

    /**
     * @return the porcentaje
     */
    public double getPorcentaje() {
        return porcentaje;
    }

    /**
     * @param porcentaje the porcentaje to set
     */
    public void setPorcentaje(double porcentaje) {
        this.porcentaje = porcentaje;
    }
}

   